
// Un metodo estatico es una funcion de la clase, que no se invoca sobre las instancias de un objeto, sino sobre la clase misma.

// Es decir, no necesita instanciarse un objeto para poder usarlo, sino que se puede llamar directamente desde la clase.

// En este caso, la clase es "Math" y el metodo es "random".

/*
NO PODEMOS HACER ESTO:
const objeto = {};

objeto.create();
objeto.assign();


SI PODEMOS HACER: Invocarlos sin crear un objeto, sino directamente desde la clase Object.
Object.create();
Object.assign();
*/