// En JavaScript no es necesario declarar el tipo de dato de una variable, se infiere en tiempo de ejecuci√≥n

// String o cadenas de texto
let apellido = "Gonzalez";

// Numeros: enteros y decimales
let edad = 30;
let precio = 15.5; // El punto es el separador decimal

// Booleanos
let esMayor = true;
let esMenor = false;

// Undefined (no definido, es decir, se declara una variable pero no se le asigna un valor)
let sinValor;

// Null (nulo, es decir, se declara una variable y se le asigna un valor nulo)
let nulo = null;
// La diferencia entre nulo y undefined, es que nulo es un valor asignado a una variable, mientras que undefined es un valor por defecto que se le asigna a una variable que no tiene valor

// Array (arreglo) es una lista de elementos, se abren y cierran corchetes y los elementos se separan por comas. Pueden tener elementos de diferentes tipos
let frutas = ["manzana", "pera", "uva"];
let numeros = [1, 2, 3, 4, 5];
let mixto = [1, "manzana", true, 3.5];

// Objetos: son colecciones de pares clave-valor, se abren y cierran llaves y los pares clave-valor se separan por comas. Se declaran con llaves y se asignan valores a las claves con dos puntos
let persona = {
  nombre: "Juan",
  edad: 30,
  esMayor: true,
};

