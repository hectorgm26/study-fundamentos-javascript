// Una libreria es un conjunto de funciones y herramientas reutilizables que facilitan el desarrollo para solucionar problemas comunes.

// Diferencias

// Modulo = Es un archivo o conjunto de archivos que exportan funcionalidades específicas y pueden ser importados en otros archivos para su uso. Es una unidad mas pequeña y especifica que una libreria.

// Libreria = Es un conjunto de funciones y herramientras reutilizables que facilitan el desarrollo para solucionar problemas comunes. Es decir, un conjunto de modulos.

// Framework = Es un conjunto de herramientas y librerias que proporcionan una estructura y convenciones para el desarrollo de aplicaciones. Un framework es mas grande y abarcativo que una libreria, ya que establece una arquitectura y flujo de trabajo completo para el desarrollo de aplicaciones.

// Paquete = Es una unidad de distribucion de codigo que pueden incluir una o varias librerias, modulos y otros recursos.

