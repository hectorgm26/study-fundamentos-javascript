const condition = true;

if (condicion) {
    console.log("La condición es verdadera");
} else {
    console.log("La condición es falsa");
}

// Operador ternario
// Son otra forma de escribir un if-else

// Sintaxis: condicion ? valorSiVerdadero : valorSiFalso

condition ? console.log("La condición es verdadera") : console.log("La condición es falsa");

// Si se cumple, se ejecuta lo que esta antes de los dos puntos, si no se cumple, se ejecuta lo que esta después de los dos puntos a la derecha