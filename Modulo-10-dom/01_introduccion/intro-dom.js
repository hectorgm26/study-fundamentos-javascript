// El DOM es la representación a traves de objetos que tiene JS de la estructura HTML
// El DOM es un árbol de nodos, donde cada nodo es un objeto que representa una parte del HTML
// DOM significa Document Object Model

// EL Dom tiene dos objetos principales: document y window
// El objeto document representa el documento HTML y nos permite acceder a los elementos del DOM
// El objeto window representa la ventana del navegador y nos permite acceder a las propiedades y métodos de la ventana
// El objeto document tiene varios métodos para acceder a los elementos del DOM

// Los métodos más comunes son:
// getElementById: devuelve el elemento con el id especificado
// getElementsByClassName: devuelve una colección de elementos con la clase especificada
// getElementsByTagName: devuelve una colección de elementos con la etiqueta especificada
// querySelector: devuelve el primer elemento que coincide con el selector CSS especificado
// querySelectorAll: devuelve una colección de elementos que coinciden con el selector CSS especificado
// createElement: crea un nuevo elemento HTML
// createTextNode: crea un nuevo nodo de texto
// appendChild: añade un nuevo nodo como hijo de un nodo existente
// removeChild: elimina un nodo hijo de un nodo existente
// replaceChild: reemplaza un nodo hijo de un nodo existente por otro nodo
// setAttribute: establece un atributo de un elemento
// getAttribute: obtiene el valor de un atributo de un elemento
// removeAttribute: elimina un atributo de un elemento
// classList: propiedad que devuelve una colección de las clases de un elemento

// classList tiene varios métodos para añadir, eliminar y comprobar clases
// add: añade una clase a un elemento
// remove: elimina una clase de un elemento
// toggle: añade o elimina una clase de un elemento, dependiendo de si ya existe o no
// contains: comprueba si un elemento tiene una clase
// replace: reemplaza una clase de un elemento por otra
// item: devuelve el nombre de una clase en una posición determinada
// length: devuelve el número de clases de un elemento
// forEach: ejecuta una función para cada clase de un elemento
// entries: devuelve un array con los nombres y valores de las clases de un elemento
// keys: devuelve un array con los nombres de las clases de un elemento
// values: devuelve un array con los valores de las clases de un elemento
// toString: devuelve una cadena con los nombres de las clases de un elemento
// toArray: convierte la colección de clases en un array
// contains: comprueba si un elemento tiene una clase
